/* Variables And Values In Scala */
object HelloWorld {
    def main(args: Array[String]) {
        val a: Int = 3
        val b = true
        var c = 'g'
        val d = 2.8
        val s = "Hi!"
        
        println("val a = " + a + " is " + a.getClass)
        println("var b = " + b + " is " + b.getClass)
        println("var c = " + c + " is " + c.getClass)
        println("var d = " + d + " is " + d.getClass)
        println("var s = " + s + " is " + s.getClass)
    }
}
